export type AppLocale = "en" | "de" | "ar" | "tr";

export const APP_LOCALES: readonly AppLocale[] = ["en", "de", "ar", "tr"];

export function normalizeAppLocale(value: string | undefined | null): AppLocale {
  const v = (value ?? "en").toLowerCase().slice(0, 2);
  if (v === "de" || v === "ar" || v === "tr" || v === "en") return v;
  return "en";
}

