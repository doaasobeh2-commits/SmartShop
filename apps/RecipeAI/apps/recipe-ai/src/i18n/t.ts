export const MESSAGES: Record<AppLocale, Catalog> = { en, de, ar, tr };

export function t(
  locale: AppLocale,
  key: MessageKey,
  vars?: Record<string, string | number>,
): string {
  const catalog = MESSAGES[locale];
  // Prefer selected language whenever the key exists there — never prefer English over it.
  let template =
    catalog[key] ??
    (locale !== "en" && MESSAGES.en[key] ? MESSAGES.en[key] : key);

  if (vars) {
    for (const [name, value] of Object.entries(vars)) {
      template = template.replaceAll(`{${name}}`, String(value));
    }
  }
  return template;
}
